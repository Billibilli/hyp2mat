/**
 * @brief updates an option
 * @param field the generic pointer to the field to update
 * @param orig_field the pointer to the orig field
 * @param field_given the pointer to the number of occurrence of this option
 * @param prev_given the pointer to the number of occurrence already seen
 * @param value the argument for this option (if null no arg was specified)
 * @param possible_values the possible values for this option (if specified)
 * @param default_value the default value (in case the option only accepts fixed values)
 * @param arg_type the type of this option
 * @param check_ambiguity @see cmdline_parser_params.check_ambiguity
 * @param override @see cmdline_parser_params.override
 * @param no_free whether to free a possible previous value
 * @param multiple_option whether this is a multiple option
 * @param long_opt the corresponding long option
 * @param short_opt the corresponding short option (or '-' if none)
 * @param additional_error possible further error specification
 */
static
int update_arg(void *field, char **orig_field,
               unsigned int *field_given, unsigned int *prev_given, 
               char *value, const char *possible_values[],
               const char *default_value,
               cmdline_parser_arg_type arg_type,
               int check_ambiguity, int override,
               int no_free, int multiple_option,
               const char *long_opt, char short_opt,
               const char *additional_error)
{
  char *stop_char = 0;
  const char *val = value;
  int found;
  char **string_field;
  FIX_UNUSED (field);

  stop_char = 0;
  found = 0;

  if (!multiple_option && prev_given && (*prev_given || (check_ambiguity && *field_given)))
    {
      if (short_opt != '-')
        fprintf (stderr, "%s: `--%s' (`-%c') option given more than once%s\n", 
               package_name, long_opt, short_opt,
               (additional_error ? additional_error : ""));
      else
        fprintf (stderr, "%s: `--%s' option given more than once%s\n", 
               package_name, long_opt,
               (additional_error ? additional_error : ""));
      return 1; /* failure */
    }

  if (possible_values && (found = check_possible_values((value ? value : default_value), possible_values)) < 0)
    {
      if (short_opt != '-')
        fprintf (stderr, "%s: %s argument, \"%s\", for option `--%s' (`-%c')%s\n", 
          package_name, (found == -2) ? "ambiguous" : "invalid", value, long_opt, short_opt,
          (additional_error ? additional_error : ""));
      else
        fprintf (stderr, "%s: %s argument, \"%s\", for option `--%s'%s\n", 
          package_name, (found == -2) ? "ambiguous" : "invalid", value, long_opt,
          (additional_error ? additional_error : ""));
      return 1; /* failure */
    }
    
  if (field_given && *field_given && ! override)
    return 0;
  if (prev_given)
    (*prev_given)++;
  if (field_given)
    (*field_given)++;
  if (possible_values)
    val = possible_values[found];

  switch(arg_type) {
  case ARG_FLAG:
    *((int *)field) = !*((int *)field);
    break;
  case ARG_DOUBLE:
    if (val) *((double *)field) = strtod (val, &stop_char);
    break;
  case ARG_ENUM:
    if (val) *((int *)field) = found;
    break;
  case ARG_STRING:
    if (val) {
      string_field = (char **)field;
      if (!no_free && *string_field)
        free (*string_field); /* free previous string */
      *string_field = gengetopt_strdup (val);
    }
    break;
  default:
    break;
  };

  /* check numeric conversion */
  switch(arg_type) {
  case ARG_DOUBLE:
    if (val && !(stop_char && *stop_char == '\0')) {
      fprintf(stderr, "%s: invalid numeric value: %s\n", package_name, val);
      return 1; /* failure */
    }
    break;
  default:
    ;
  };

  /* store the original value */
  switch(arg_type) {
  case ARG_NO:
  case ARG_FLAG:
    break;
  default:
    if (value && orig_field) {
      if (no_free) {
        *orig_field = value;
      } else {
        if (*orig_field)
          free (*orig_field); /* free previous string */
        *orig_field = gengetopt_strdup (value);
      }
    }
  };

  return 0; /* OK */
}

/**
 * @brief store information about a multiple option in a temporary list
 * @param list where to (temporarily) store multiple options
 */
static
int update_multiple_arg_temp(struct generic_list **list,
               unsigned int *prev_given, const char *val,
               const char *possible_values[], const char *default_value,
               cmdline_parser_arg_type arg_type,
               const char *long_opt, char short_opt,
               const char *additional_error)
{
  /* store single arguments */
  char *multi_token;
  const char *multi_next;

  if (arg_type == ARG_NO) {
    (*prev_given)++;
    return 0; /* OK */
  }

  multi_token = get_multiple_arg_token(val);
  multi_next = get_multiple_arg_token_next (val);

  while (1)
    {
      add_node (list);
      if (update_arg((void *)&((*list)->arg), &((*list)->orig), 0,
          prev_given, multi_token, possible_values, default_value, 
          arg_type, 0, 1, 1, 1, long_opt, short_opt, additional_error)) {
        if (multi_token) free(multi_token);
        return 1; /* failure */
      }

      if (multi_next)
        {
          multi_token = get_multiple_arg_token(multi_next);
          multi_next = get_multiple_arg_token_next (multi_next);
        }
      else
        break;
    }

  return 0; /* OK */
}

/**
 * @brief free the passed list (including possible string argument)
 */
static
void free_list(struct generic_list *list, short string_arg)
{
  if (list) {
    struct generic_list *tmp;
    while (list)
      {
        tmp = list;
        if (string_arg && list->arg.string_arg)
          free (list->arg.string_arg);
        if (list->orig)
          free (list->orig);
        list = list->next;
        free (tmp);
      }
  }
}

/**
 * @brief updates a multiple option starting from the passed list
 */
static
void update_multiple_arg(void *field, char ***orig_field,
               unsigned int field_given, unsigned int prev_given, union generic_value *default_value,
               cmdline_parser_arg_type arg_type,
               struct generic_list *list)
{
  int i;
  struct generic_list *tmp;

  if (prev_given && list) {
    *orig_field = (char **) realloc (*orig_field, (field_given + prev_given) * sizeof (char *));

    switch(arg_type) {
    case ARG_ENUM:
      *((int **)field) = (int *)realloc (*((int **)field), (field_given + prev_given) * sizeof (int)); break;
    case ARG_DOUBLE:
      *((double **)field) = (double *)realloc (*((double **)field), (field_given + prev_given) * sizeof (double)); break;
    case ARG_STRING:
      *((char ***)field) = (char **)realloc (*((char ***)field), (field_given + prev_given) * sizeof (char *)); break;
    default:
      break;
    };
    
    for (i = (prev_given - 1); i >= 0; --i)
      {
        tmp = list;
        
        switch(arg_type) {
        case ARG_DOUBLE:
          (*((double **)field))[i + field_given] = tmp->arg.double_arg; break;
        case ARG_ENUM:
          (*((int **)field))[i + field_given] = tmp->arg.int_arg; break;
        case ARG_STRING:
          (*((char ***)field))[i + field_given] = tmp->arg.string_arg; break;
        default:
          break;
        }        
        (*orig_field) [i + field_given] = list->orig;
        list = list->next;
        free (tmp);
      }
  } else { /* set the default value */
    if (default_value && ! field_given) {
      switch(arg_type) {
      case ARG_ENUM:
        if (! *((int **)field)) {
          *((int **)field) = (int *)malloc (sizeof (int));
          (*((int **)field))[0] = default_value->int_arg; 
        }
        break;
      case ARG_DOUBLE:
        if (! *((double **)field)) {
          *((double **)field) = (double *)malloc (sizeof (double));
          (*((double **)field))[0] = default_value->double_arg;
        }
        break;
      case ARG_STRING:
        if (! *((char ***)field)) {
          *((char ***)field) = (char **)malloc (sizeof (char *));
          (*((char ***)field))[0] = gengetopt_strdup(default_value->string_arg);
        }
        break;
      default: break;
      }
      if (!(*orig_field)) {
        *orig_field = (char **) malloc (sizeof (char *));
        (*orig_field)[0] = 0;
      }
    }
  }
}

int
cmdline_parser_internal (
  int argc, char **argv, struct gengetopt_args_info *args_info,
                        struct cmdline_parser_params *params, const char *additional_error)
{
  int c;	/* Character of the parsed option.  */

  struct generic_list * net_list = NULL;
  int error = 0;
  struct gengetopt_args_info local_args_info;
  
  int override;
  int initialize;
  int check_required;
  int check_ambiguity;
  
  package_name = argv[0];
  
  override = params->override;
  initialize = params->initialize;
  check_required = params->check_required;
  check_ambiguity = params->check_ambiguity;

  if (initialize)
    cmdline_parser_init (args_info);

  cmdline_parser_init (&local_args_info);

  optarg = 0;
  optind = 0;
  opterr = params->print_errors;
  optopt = '?';

  while (1)
    {
      int option_index = 0;

      static struct option long_options[] = {
        { "help",	0, NULL, 'h' },
        { "version",	0, NULL, 'V' },
        { "output",	1, NULL, 'o' },
        { "output-format",	1, NULL, 'f' },
        { "net",	1, NULL, 'n' },
        { "epsilonr",	1, NULL, 'e' },
        { "xmin",	1, NULL, 'x' },
        { "xmax",	1, NULL, 'X' },
        { "ymin",	1, NULL, 'y' },
        { "ymax",	1, NULL, 'Y' },
        { "zmin",	1, NULL, 'z' },
        { "zmax",	1, NULL, 'Z' },
        { "grid",	1, NULL, 'g' },
        { "arc-precision",	1, NULL, 'p' },
        { "hue",	1, NULL, 0 },
        { "saturation",	1, NULL, 0 },
        { "brightness",	1, NULL, 0 },
        { "raw",	0, NULL, 'r' },
        { "debug",	0, NULL, 'd' },
        { "verbose",	0, NULL, 'v' },
        { 0,  0, 0, 0 }
      };

      c = getopt_long (argc, argv, "hVo:f:n:e:x:X:y:Y:z:Z:g:p:rdv", long_options, &option_index);

      if (c == -1) break;	/* Exit from `while (1)' loop.  */

      switch (c)
        {
        case 'h':	/* Print help and exit.  */
          cmdline_parser_print_help ();
          cmdline_parser_free (&local_args_info);
          exit (EXIT_SUCCESS);

        case 'V':	/* Print version and exit.  */
          cmdline_parser_print_version ();
          cmdline_parser_free (&local_args_info);
          exit (EXIT_SUCCESS);

        case 'o':	/* Output file..  */
        
        
          if (update_arg( (void *)&(args_info->output_arg), 
               &(args_info->output_orig), &(args_info->output_given),
              &(local_args_info.output_given), optarg, 0, "-", ARG_STRING,
              check_ambiguity, override, 0, 0,
              "output", 'o',
              additional_error))
            goto failure;
        
          break;
        case 'f':	/* Output file format..  */
        
        
          if (update_arg( (void *)&(args_info->output_format_arg), 
               &(args_info->output_format_orig), &(args_info->output_format_given),
              &(local_args_info.output_format_given), optarg, cmdline_parser_output_format_values, "pdf", ARG_ENUM,
              check_ambiguity, override, 0, 0,
              "output-format", 'f',
              additional_error))
            goto failure;
        
          break;
        case 'n':	/* Import named net. Repeat to import several nets. If no nets are specified all nets are imported..  */
        
          if (update_multiple_arg_temp(&net_list, 
              &(local_args_info.net_given), optarg, 0, 0, ARG_STRING,
              "net", 'n',
              additional_error))
            goto failure;
        
          break;
        case 'e':	/* Set dielectric epsilon r..  */
        
        
          if (update_arg( (void *)&(args_info->epsilonr_arg), 
               &(args_info->epsilonr_orig), &(args_info->epsilonr_given),
              &(local_args_info.epsilonr_given), optarg, 0, 0, ARG_DOUBLE,
              check_ambiguity, override, 0, 0,
              "epsilonr", 'e',
              additional_error))
            goto failure;
        
          break;
        case 'x':	/* Crop pcb. Set lowest value of x coordinate..  */
        
        
          if (update_arg( (void *)&(args_info->xmin_arg), 
               &(args_info->xmin_orig), &(args_info->xmin_given),
              &(local_args_info.xmin_given), optarg, 0, 0, ARG_DOUBLE,
              check_ambiguity, override, 0, 0,
              "xmin", 'x',
              additional_error))
            goto failure;
        
          break;
        case 'X':	/* Crop pcb. Set highest value of x coordinate..  */
        
        
          if (update_arg( (void *)&(args_info->xmax_arg), 
               &(args_info->xmax_orig), &(args_info->xmax_given),
              &(local_args_info.xmax_given), optarg, 0, 0, ARG_DOUBLE,
              check_ambiguity, override, 0, 0,
              "xmax", 'X',
              additional_error))
            goto failure;
        
          break;
        case 'y':	/* Crop pcb. Set lowest value of y coordinate..  */
        
        
          if (update_arg( (void *)&(args_info->ymin_arg), 
               &(args_info->ymin_orig), &(args_info->ymin_given),
              &(local_args_info.ymin_given), optarg, 0, 0, ARG_DOUBLE,
              check_ambiguity, override, 0, 0,
              "ymin", 'y',
              additional_error))
            goto failure;
        
          break;
        case 'Y':	/* Crop pcb. Set highest value of y coordinate..  */
        
        
          if (update_arg( (void *)&(args_info->ymax_arg), 
               &(args_info->ymax_orig), &(args_info->ymax_given),
              &(local_args_info.ymax_given), optarg, 0, 0, ARG_DOUBLE,
              check_ambiguity, override, 0, 0,
              "ymax", 'Y',
              additional_error))
            goto failure;
        
          break;
        case 'z':	/* Crop pcb. Set lowest value of z coordinate..  */
        
        
          if (update_arg( (void *)&(args_info->zmin_arg), 
               &(args_info->zmin_orig), &(args_info->zmin_given),
              &(local_args_info.zmin_given), optarg, 0, 0, ARG_DOUBLE,
              check_ambiguity, override, 0, 0,
              "zmin", 'z',
              additional_error))
            goto failure;
        
          break;
        case 'Z':	/* Crop pcb. Set highest value of z coordinate..  */
        
        
          if (update_arg( (void *)&(args_info->zmax_arg), 
               &(args_info->zmax_orig), &(args_info->zmax_given),
              &(local_args_info.zmax_given), optarg, 0, 0, ARG_DOUBLE,
              check_ambiguity, override, 0, 0,
              "zmax", 'Z',
              additional_error))
            goto failure;
        
          break;
        case 'g':	/* Set output grid size..  */
        
        
          if (update_arg( (void *)&(args_info->grid_arg), 
               &(args_info->grid_orig), &(args_info->grid_given),
              &(local_args_info.grid_given), optarg, 0, "10e-6", ARG_DOUBLE,
              check_ambiguity, override, 0, 0,
              "grid", 'g',
              additional_error))
            goto failure;
        
          break;
        case 'p':	/* Set maximum difference between perfect arc and polygonal approximation..  */
        
        
          if (update_arg( (void *)&(args_info->arc_precision_arg), 
               &(args_info->arc_precision_orig), &(args_info->arc_precision_given),
              &(local_args_info.arc_precision_given), optarg, 0, "0", ARG_DOUBLE,
              check_ambiguity, override, 0, 0,
              "arc-precision", 'p',
              additional_error))
            goto failure;
        
          break;
        case 'r':	/* Raw output. Do not join adjacent or overlapping copper. Do not invert planes..  */
        
        
          if (update_arg((void *)&(args_info->raw_flag), 0, &(args_info->raw_given),
              &(local_args_info.raw_given), optarg, 0, 0, ARG_FLAG,
              check_ambiguity, override, 1, 0, "raw", 'r',
              additional_error))
            goto failure;
        
          break;
        case 'd':	/* Increase debugging level. Repeat option for more detailed debugging..  */
        
          local_args_info.debug_given++;
        
          break;
        case 'v':	/* Print board summary..  */
        
        
          if (update_arg( 0 , 
               0 , &(args_info->verbose_given),
              &(local_args_info.verbose_given), optarg, 0, 0, ARG_NO,
              check_ambiguity, override, 0, 0,
              "verbose", 'v',
              additional_error))
            goto failure;
        
          break;

        case 0:	/* Long option with no short option */
          /* Set PDF color hue. Range 0.0 to 1.0.  */
          if (strcmp (long_options[option_index].name, "hue") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->hue_arg), 
                 &(args_info->hue_orig), &(args_info->hue_given),
                &(local_args_info.hue_given), optarg, 0, "0", ARG_DOUBLE,
                check_ambiguity, override, 0, 0,
                "hue", '-',
                additional_error))
              goto failure;
          
          }
          /* Set PDF color saturation. Range 0.0 to 1.0.  */
          else if (strcmp (long_options[option_index].name, "saturation") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->saturation_arg), 
                 &(args_info->saturation_orig), &(args_info->saturation_given),
                &(local_args_info.saturation_given), optarg, 0, "0.6", ARG_DOUBLE,
                check_ambiguity, override, 0, 0,
                "saturation", '-',
                additional_error))
              goto failure;
          
          }
          /* Set PDF color brightness. Range 0.0 to 1.0.  */
          else if (strcmp (long_options[option_index].name, "brightness") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->brightness_arg), 
                 &(args_info->brightness_orig), &(args_info->brightness_given),
                &(local_args_info.brightness_given), optarg, 0, "0.9", ARG_DOUBLE,
                check_ambiguity, override, 0, 0,
                "brightness", '-',
                additional_error))
              goto failure;
          
          }
          
          break;
        case '?':	/* Invalid option.  */
          /* `getopt_long' already printed an error message.  */
          goto failure;

        default:	/* bug: option not considered.  */
          fprintf (stderr, "%s: option unknown: %c%s\n", CMDLINE_PARSER_PACKAGE, c, (additional_error ? additional_error : ""));
          abort ();
        } /* switch */
    } /* while */


  update_multiple_arg((void *)&(args_info->net_arg),
    &(args_info->net_orig), args_info->net_given,
    local_args_info.net_given, 0,
    ARG_STRING, net_list);

  args_info->net_given += local_args_info.net_given;
  local_args_info.net_given = 0;
  args_info->debug_given += local_args_info.debug_given;
  local_args_info.debug_given = 0;
  
  if (check_required)
    {
      error += cmdline_parser_required2 (args_info, argv[0], additional_error);
    }

  cmdline_parser_release (&local_args_info);

  if ( error )
    return (EXIT_FAILURE);

  if (optind < argc)
    {
      int i = 0 ;
      int found_prog_name = 0;
      /* whether program name, i.e., argv[0], is in the remaining args
         (this may happen with some implementations of getopt,
          but surely not with the one included by gengetopt) */

      i = optind;
      while (i < argc)
        if (argv[i++] == argv[0]) {
          found_prog_name = 1;
          break;
        }
      i = 0;

      args_info->inputs_num = argc - optind - found_prog_name;
      args_info->inputs =
        (char **)(malloc ((args_info->inputs_num)*sizeof(char *))) ;
      while (optind < argc)
        if (argv[optind++] != argv[0])
          args_info->inputs[ i++ ] = gengetopt_strdup (argv[optind-1]) ;
    }

  return 0;

failure:
  free_list (net_list, 1 );
  
  cmdline_parser_release (&local_args_info);
  return (EXIT_FAILURE);
}
